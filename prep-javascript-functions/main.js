function addTwoNumbers(num1, num2) {
  return num1 + num2;
}
const addTwoNumbersResult = addTwoNumbers(2, 2);
console.log('Add two numbers exercise:', addTwoNumbersResult);

function convertHoursToMinutes(hours) {
  return hours * 60;
}
const convertHoursToMinutesResult = convertHoursToMinutes(2);
console.log('Convert hours to minutes exercise:', convertHoursToMinutesResult);

function getGreeting(name) {
  return 'Hello' + ' ' + name + '!';
}
const getGreetingResult = getGreeting('Beautiful');
console.log('Greeting exercise:', getGreetingResult);

function addAndMultiplyby5(num1, num2) {
  return (num1 + num2) * 5;
}
const addAndMultiplyby5Result = addAndMultiplyby5(10, 5);
console.log('add and multiply by 5 exercise:', addAndMultiplyby5Result);

function multiplyAndDivideby5(num1, num2) {
  return (num1 * num2) / 5;
}
const multiplyAndDivideby5Result = multiplyAndDivideby5(35, 10);
console.log('Multiply and divide by 5 exercise:', multiplyAndDivideby5Result);

function subtractTwoNumbers(num1, num2) {
  return num1 - num2;
}
const subtractTwoNumbersResult = subtractTwoNumbers(22, 7);
console.log('subtract two numbers exercise:', subtractTwoNumbersResult);

function getCircleCircumference(radius) {
  return 2 * 3.141592653589793 * radius;
}
const getCircleCircumferenceResult = getCircleCircumference(5);
console.log('get circle circumference exercise:', getCircleCircumferenceResult);

function getFullName(firstName, lastName) {
  return firstName + ' ' + lastName;
}
const getFullNameResult = getFullName('Jeff', 'Probst');
console.log('get full name exercise:', getFullNameResult);

function cube(number) {
  return number * number * number;
}
const cubeResult = cube(5);
console.log('cube exercise:', cubeResult);
